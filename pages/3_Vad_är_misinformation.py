import streamlit as st

st.title(" Vad är misinformation?")

st.markdown("""
I dagens digitala samhälle delas information snabbare än någonsin. Men all information vi ser online är inte sann. **Misinformation** betyder att man får felaktig eller vilseledande information – ibland av misstag, ibland med mening.

###  Varför är misinformation ett problem?

-  Du kan bli lurad att tro på något som inte stämmer.
-  Det kan påverka hur du tänker kring viktiga saker som hälsa, politik och vetenskap.
-  Det sprider sig snabbt – särskilt på sociala medier.
-  Det gör det svårare att veta vad som är sant och vad som bara *låter* sant.

###  Exempel på misinformation:

- En rubrik som säger att choklad botar covid.
- En bild på ett citat som någon känd person aldrig har sagt.
- En nyhet som säger att en känd person är död – men det är inte sant.

###  Vad kan man göra åt det?

- Vara källkritisk – **vem säger det?**
- Leta efter flera källor – säger fler samma sak?
- Använd verktyg som **Källkritikradarn** för att få stöd.

---

>  Genom att förstå vad misinformation är, kan du bli en bättre informationsanvändare – och hjälpa andra att inte bli lurade.
""")

st.markdown("---")